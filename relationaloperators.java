public class relationaloperators {
    public static void main(String[] args) {
        int p = 45, q = 25;
        System.out.println(p==q);
        System.out.println(p!=q);
        System.out.println(p<=q);
        System.out.println(p>=q);
        System.out.println(p<q);
        System.out.println(p>q);
    }
    
}

